// 10/3 Charly. Print the Fight Song without using println in the main method.
public class FightSong {
    private static void go(){
        System.out.println("Go, team, go! \nYou can do it.");
    }
    private static void spaceLine(){
        System.out.println(" ");
    }
    private static void you(){
        System.out.println("You're the best,\nIn the West.");
    }
    public static void main(String[] args){
        go();
        spaceLine();
        go();
        you();
        go();
        spaceLine();
        go();
        you();
        go();
        spaceLine();
        go();
    }
}
